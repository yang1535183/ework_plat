package com.diiwon.mis.plat.base.vojo;

public class BasicCodeManager {
	
	private String platTitle;
	
	private String platName;
	
	private String platUrl;
	
	private String platAdcode;
	
	private String platAdname;
	
	
	public String getPlatTitle() {
		return platTitle;
	}

	public void setPlatTitle(String platTitle) {
		this.platTitle = platTitle;
	}

	public String getPlatName() {
		return platName;
	}

	public void setPlatName(String platName) {
		this.platName = platName;
	}

	public String getPlatUrl() {
		return platUrl;
	}

	public void setPlatUrl(String platUrl) {
		this.platUrl = platUrl;
	}

	public String getPlatAdcode() {
		return platAdcode;
	}

	public void setPlatAdcode(String platAdcode) {
		this.platAdcode = platAdcode;
	}

	public String getPlatAdname() {
		return platAdname;
	}

	public void setPlatAdname(String platAdname) {
		this.platAdname = platAdname;
	}
	
}
